    private void jLSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLSalvarMouseClicked
        try {
            if (acao == 2) {
                if (validarCampos()) {
                    if (preencherObjeto()) {
                        if (DAO.editar(cat)) {
                            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!", "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                            //jbCancelarActionPerformed(evt);
                            //JIFCategoria.tabelaini();
                            dispose();
                        } else {
                            JOptionPane.showMessageDialog(this, "Não foi possível atualizar!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
                            dispose();
                        }
                    }
                }
            } else {
                if (validarCampos()) {
                    if (preencherObjeto()) {
                        if (DAO.incluir(cat)) {
                            JOptionPane.showMessageDialog(this, "Salvo com sucesso!", "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                            dispose();
                            //jbCancelarActionPerformed(evt);
                        } else {
                            JOptionPane.showMessageDialog(this, "Erro ao salvar!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
                            dispose();
                        }
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            dispose();
        }
    }//GEN-LAST:event_jLSalvarMouseClicked

