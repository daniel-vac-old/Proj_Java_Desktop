    private boolean validarCampos() {

        String msg = "";

        if (jTNomeCat.getText().length() < 1) {
            msg += "Informe um nome.";
        }

        if (jTComissaoCat.getText().equalsIgnoreCase("")) {
            jTComissaoCat.setText("0");
            //msg += "\nInforme um email válido.";
        }

        if (msg.length() == 0) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Corrija: " + msg, "Meu sistema", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean preencherObjeto() {

        cat.setNomecat(jTNomeCat.getText());
        cat.setComissao(Double.parseDouble(jTComissaoCat.getText()));
        return true;
    }

    private void alteracao() {
        if (acao == 2) {
            jTNomeCat.setText(cat.getNomecat());
            jTComissaoCat.setText(String.valueOf(cat.getComissao()));
        }
    }

