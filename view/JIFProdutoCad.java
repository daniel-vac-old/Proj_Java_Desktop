    private void alteracao() {
        if (acao == 2) {
            jTPreco.setText(String.valueOf(pro.getPreco()));
            jTQuant.setText(String.valueOf(pro.getQuantidade()));
            jTProd.setText(pro.getNomepro());
            jCFor.setSelectedItem(pro.getFornecedor().getNomefor());
            jCCat.setSelectedItem(pro.getCategoria().getNomecat());
        }
    }
    
    private boolean validarCampos() {

        String msg = "";

        if (jTPreco.getText().length() < 1) {
            msg += "Informe o pre√ßo do produto.";
        }
        if (jTProd.getText().length() < 1) {
            msg += "Informe o nome do Produto.";
        }
        if (jTQuant.getText().length() < 1) {
            msg += "Informe a quantidade do produto.";
        }
        if (jCCat.getSelectedIndex() == 0) {
            msg += "\nInforme uma categoria.";
        }
        if (jCFor.getSelectedIndex() == 0) {
            msg += "\nInforme o fornecedor.";
        }

        if (msg.length() == 0) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Corrija: " + msg, "Meu sistema", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    
    private boolean preencherObjeto() {
        
        pro.setCategoria(listaDeCategorias.get(jCCat.getSelectedIndex() - 1));
        pro.setFornecedor(listaDeFornecedores.get(jCFor.getSelectedIndex() - 1));
        pro.setNomepro(jTProd.getText());
        pro.setPreco(Double.parseDouble(jTPreco.getText()));
        pro.setQuantidade(Integer.parseInt(jTQuant.getText()));
        return true;
    }

