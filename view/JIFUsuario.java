    private void jLAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLAlterarMouseClicked
        int linhaSelecionada = jtbDados.getSelectedRow();
        if ((use != null) && (linhaSelecionada > -1)) {
            JIFUsuarioData janela = new JIFUsuarioData(listaDeUsuarios.get(linhaSelecionada), 2);
            getParent().add(janela);
            janela.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Usuário Selecionado!", "Meu sistema",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLAlterarMouseClicked

    private void jLExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLExcluirMouseClicked
        int linhaSelecionada = jtbDados.getSelectedRow();
        if ((use != null) && (linhaSelecionada > -1)) {
            try {
                if (listaDeUsuarios.get(linhaSelecionada).getTipoUser() == 2) {
                    JOptionPane.showMessageDialog(this, "Impossível excluir um Gerente!");

                } else {
                    if (DAO.excluir(listaDeUsuarios.get(linhaSelecionada).getIdUser())) {
                        JOptionPane.showMessageDialog(this, "Excluído com sucesso!", "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                        tabelaini();
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Usuário Selecionado!", "Meu sistema",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLExcluirMouseClicked

    private void tabelaini() {
        try {
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.setNumRows(0);
            listaDeUsuarios = DAO.pesquisarGeral();
            for (Usuario use : listaDeUsuarios) {
                mp.addRow(new String[]{"" + use.getIdUser(), use.getNomeUser(), use.getUserUser(), use.getTelUser() + ""});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }


