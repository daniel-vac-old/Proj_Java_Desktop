    private void jtfPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisaKeyReleased
        try {
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.setNumRows(0);
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                listaDeUsuarios = DAO.pesquisarGeral();
                for (Usuario use : listaDeUsuarios) {
                    mp.addRow(new String[]{"" + use.getIdUser(), use.getNomeUser(), use.getUserUser(), use.getTelUser() + ""});
                }
            }
            if (jtfPesquisa.getText().length() > 1) {
                listaDeUsuarios = DAO.pesquisar(jtfPesquisa.getText());
                for (Usuario use : listaDeUsuarios) {
                    mp.addRow(new String[]{"" + use.getIdUser(), use.getNomeUser(), use.getUserUser(), use.getTelUser() + ""});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtfPesquisaKeyReleased

    private void jtfPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtfPesquisaMouseClicked
        try {
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.setNumRows(0);

            listaDeUsuarios = DAO.pesquisarGeral();
            for (Usuario u : listaDeUsuarios) {
                mp.addRow(new String[]{"" + u.getIdUser(), u.getNomeUser(), "" + u.getUserUser(), "" + u.getTelUser()});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtfPesquisaMouseClicked

