    private void tabelaini(int i) {
        try {
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.setNumRows(0);
            listaDeVendas = DOAVenpro.pesquisar(i);;
            for (VendaProduto ven : listaDeVendas) {
                mp.addRow(new String[]{"" + ven.getIdvenda(), ven.getQuantidade()+ ""});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }
