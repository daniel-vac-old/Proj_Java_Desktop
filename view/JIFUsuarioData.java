    private boolean validarCampos() {

        String msg = "";

        if (jTNome.getText().length() < 1) {
            msg += "Informe o nome do usu치rio./n";
        }
        if (jTUser.getText().length() < 1) {
            msg += "Informe o usu치rio./n";
        }
        if (jFSal.getText().length() < 1) {
            msg += "Informe o sal치rio do usu치rio./n";
        }
        if (jFTel.getText().length() < 1) {
            msg += "Informe o telefone do usu치rio./n";
        }
        if (jPSenhaUser.getText().length() < 1) {
            msg += "Informe a senha do usu치rio./n";
        }
        if (jCTipo.getSelectedIndex() == 0) {
            msg += "Informe o tipo do usu치rio./n";
        }

        if (msg.length() == 0) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Corrija: " + msg, "Meu sistema", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    
     private boolean preencherObjeto() {
        use.setNomeUser(jTNome.getText());
        use.setPassUser(jPSenhaUser.getText());
        use.setSalUser(Double.parseDouble(jFSal.getText()));
        use.setTelUser(jFTel.getText());
        use.setUserUser(jTUser.getText());
        if(jCTipo.getSelectedItem().equals("Vendedor")){
                use.setTipoUser(1);
            }else{
                use.setTipoUser(2);
            }
        return true;
    }
    
    private void alteracao() {
        if (acao == 2) {
            jTNome.setText(use.getNomeUser());
            jTUser.setText(use.getUserUser());
            if(use.getTipoUser()==1){
                jCTipo.setSelectedItem("Vendedor");
            }else{
                jCTipo.setSelectedItem("Gerente");
            }
            jFSal.setText(String.valueOf(use.getSalUser()));
            jFTel.setText(use.getTelUser());
            jPSenhaUser.setText(use.getPassUser());    
        }
    }
