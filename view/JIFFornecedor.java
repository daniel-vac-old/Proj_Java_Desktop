    private void tabelaini() {
        try {
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.setNumRows(0);
            listaDeFornecedores = DAO.pesquisarGeral();
            for (Fornecedor forn : listaDeFornecedores) {
                mp.addRow(new String[]{"" + forn.getNomefor(), forn.getRazfor(), forn.getCnpj()+ ""});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage(), "Erro ao pesquisar", JOptionPane.ERROR_MESSAGE);
        }
    }
