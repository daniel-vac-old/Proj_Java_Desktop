    private void jLSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLSalvarMouseClicked
        try {
            if (validarCampos()) {
                if (preencherObjetoVenda()) {
                    if (DAOVen.incluir(ven)) {
                        JOptionPane.showMessageDialog(this, "Venda realizada com sucesso!", "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                        dispose();
                        //jbCancelarActionPerformed(evt);
                    } else {
                        JOptionPane.showMessageDialog(this, "Erro ao salvar a venda!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
                        dispose();
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());        
        }

        for (int i = 0; i < jtbDados.getRowCount(); i++) {
            try {
                if (preencherObjetoVendaPro(i)) {
                    if (DOAVenpro.incluir(venpro)) {
                        System.out.println("Salvo com sucesso nxn! "+i);
                        //JOptionPane.showMessageDialog(this, "Salvo com sucesso!", "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                        dispose();
                        //jbCancelarActionPerformed(evt);
                    } else {
                        System.out.println("Erro ao salvar! nxn! "+i);
                        //JOptionPane.showMessageDialog(this, "Erro ao salvar!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
                        dispose();
                    }
                    DAOProd.editarvenda(Integer.parseInt(String.valueOf(jtbDados.getValueAt(i, 2))), Integer.parseInt(String.valueOf(jtbDados.getValueAt(i, 0))));
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
                dispose();
                Checkout janela = new Checkout();
                getParent().add(janela);
                janela.setVisible(true);
            }
        }
    }//GEN-LAST:event_jLSalvarMouseClicked

