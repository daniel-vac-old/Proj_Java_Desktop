    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double tes, aux;
        if (checar()) {
            //pro.getPreco(listaDeProdutos.get(jCProd.getSelectedIndex() - 1).getPreco());
            tes = listaDeProdutos.get(jCProd.getSelectedIndex() - 1).getPreco();
            aux = tes * (Integer.parseInt(jTQuantidade.getText()));
            precotot += aux;
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            mp.addRow(new String[]{"" + listaDeProdutos.get(jCProd.getSelectedIndex() - 1).getIdpro(), listaDeProdutos.get(jCProd.getSelectedIndex() - 1).getNomepro(), jTQuantidade.getText(), String.valueOf(listaDeProdutos.get(jCProd.getSelectedIndex() - 1).getPreco()) + ""});
            jLTotal.setText(String.valueOf(precotot));
            jCProd.setSelectedIndex(0);
            jTQuantidade.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linhaSelecionada = jtbDados.getSelectedRow();
        if (linhaSelecionada > -1) {
            double aux1, aux2;
            DefaultTableModel mp = (DefaultTableModel) jtbDados.getModel();
            aux1 = Double.parseDouble(String.valueOf(jtbDados.getValueAt(jtbDados.getSelectedRow(), 2)));
            aux2 = Double.parseDouble(String.valueOf(jtbDados.getValueAt(jtbDados.getSelectedRow(), 3)));
            precotot -= (aux1 * aux2);
            jLTotal.setText(String.valueOf(precotot));
            mp.removeRow(jtbDados.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um produto para excluir!", "Meu sistema",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    private boolean validarCampos() {
        if (jtbDados.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Nenhum produto adicionado na venda!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

    private boolean preencherObjetoVenda() {
        ven.setTotalvenda(Double.parseDouble(jLTotal.getText()));
        return true;
    }
    
    private boolean preencherObjetoVendaPro(int i) throws Exception {
        int aux = 0;
        try{
            aux = Integer.parseInt(DAOVen.ultimavenda());
        }catch (NumberFormatException e){
            System.out.println("Venda não encontrada");
        }
        venpro.setIdpro(Integer.parseInt(String.valueOf(jtbDados.getValueAt(i, 0))));
        venpro.setIdvenda(aux);
        venpro.setQuantidade(Integer.parseInt(String.valueOf(jtbDados.getValueAt(i, 2))));
        return true;
    }

    private boolean checar() {
        int teste = 0;
        if (jCProd.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Informe um Produto!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
            teste++;
        }
        try {
            Integer.parseInt(jTQuantidade.getText());
        } catch (NumberFormatException e) {
            teste++;
            JOptionPane.showMessageDialog(this, "Informe uma Quantidade Valida!", "Meu sistema", JOptionPane.ERROR_MESSAGE);
        }
        if (teste == 0) {
            return true;
        } else {
            return false;
        }
    }
